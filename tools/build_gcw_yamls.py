"""
build_gcw_yamls.py — author the data/worlds/gcw/*.yaml counterpart files
by reading the legacy GCW source-of-truth out of engine/world_lore.py
and engine/director.py.

The output YAMLs are byte-equivalent (at the parsed-dict level) to the
legacy hardcoded constants. They are the regression-test asset that
Drop 6a.2 and 6a.3 require to gate the engine refactor.

Run from inside the SW_MUSH repo root:
    python3 build_gcw_yamls.py

Outputs:
    data/worlds/gcw/lore.yaml
    data/worlds/gcw/director_config.yaml
    data/worlds/gcw/ambient_events.yaml
    data/worlds/gcw/era.yaml
"""

from __future__ import annotations


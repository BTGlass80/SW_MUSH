AMBIENT_HEADER = """\
# data/worlds/gcw/ambient_events.yaml
# Galactic Civil War Era — Ambient Room Events
# Schema version 1
#
# This file is the GCW counterpart to
# data/worlds/clone_wars/ambient_events.yaml. It is BYTE-EQUIVALENT to
# the legacy data/ambient_events.yaml at the time Drop 6a.5 was authored.
#
# When Drop 6a.4 lands and engine/ambient_events.py reads from era YAML,
# this file becomes the live ambient pool for active_era==gcw. Until then,
# it is the regression-test asset for byte-equivalence.
#
# The CW ambient_events.yaml authors per-zone keys (e.g. coruscant_senate);
# this GCW file authors the legacy generic keys: spaceport, streets,
# cantina, shops, jabba, government, default. Engine refactor 6a.4 makes
# the merge logic era-aware so both shapes coexist.

schema_version: 1

ambient_events:
"""


def write_ambient_events_yaml(global_path: Path, out: Path) -> None:
    """Mirror data/ambient_events.yaml under data/worlds/gcw/ambient_events.yaml.

    The legacy file uses bare top-level keys (spaceport: [], streets: [], ...).
    The CW counterpart wraps them under `ambient_events:`. We adopt the CW shape
    for consistency.
    """
    legacy = yaml.safe_load(global_path.read_text())

    Dumper = _make_dumper()
    body = yaml.dump(
        legacy,
        Dumper=Dumper,
        sort_keys=False,
        default_flow_style=False,
        allow_unicode=True,
        width=120,
    )
    indented = "\n".join("  " + line if line else "" for line in body.splitlines())
    out.write_text(AMBIENT_HEADER + indented + "\n")



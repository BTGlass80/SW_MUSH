# ── main ─────────────────────────────────────────────────────────────────────


def main():
    repo_root = Path(__file__).resolve().parent
    if not (repo_root / "engine" / "director.py").is_file():
        # Allow running from /home/claude/drop_6a5 against an external repo
        repo_root = Path(os.environ.get("SW_MUSH_REPO", "/home/claude/sw_mush/SW_MUSH"))
    if not (repo_root / "engine" / "director.py").is_file():
        print(f"ERROR: cannot find engine/director.py — set SW_MUSH_REPO=...", file=sys.stderr)
        sys.exit(1)

    out_dir = Path(__file__).resolve().parent / "data" / "worlds" / "gcw"
    out_dir.mkdir(parents=True, exist_ok=True)

    print(f"Reading source from: {repo_root}")
    print(f"Writing output to:   {out_dir}")
    print()

    # 1. Lore
    seed_entries = load_seed_entries(repo_root)
    write_lore_yaml(seed_entries, out_dir / "lore.yaml")
    print(f"  ✓ lore.yaml             ({len(seed_entries)} entries)")

    # 2. Director config
    consts = load_director_constants(repo_root)
    write_director_config_yaml(consts, out_dir / "director_config.yaml")
    print(f"  ✓ director_config.yaml  ({len(consts['DEFAULT_INFLUENCE'])} zones, "
          f"{len(consts['VALID_FACTIONS'])} factions, "
          f"{len(consts['ERA_MILESTONES'])} milestones)")

    # 3. Ambient events
    write_ambient_events_yaml(repo_root / "data" / "ambient_events.yaml",
                              out_dir / "ambient_events.yaml")
    print(f"  ✓ ambient_events.yaml")

    # 4. Era root config
    write_era_yaml(out_dir / "era.yaml")
    print(f"  ✓ era.yaml")

    print()
    print("Build complete.")


if __name__ == "__main__":
    main()
